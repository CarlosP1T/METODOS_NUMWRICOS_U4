    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (N.getText().equals("") || A.getText().equals("") || B.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Llene los campos solicitados");
        }
        if (!N.getText().equals("") & !A.getText().equals("") & !B.getText().equals("")) {
            double n = Double.parseDouble(N.getText());
            double a = Double.parseDouble(A.getText());
            double b = Double.parseDouble(B.getText());
            int invertir = 0;
            if (a > b) {
                invertir = 1;
                a = Double.parseDouble(B.getText());
                b = Double.parseDouble(A.getText());
            }
            if (n % 2 != 0) {
                JOptionPane.showMessageDialog(null, "N debe ser un numero Par");
            }
            
            if (n % 2 == 0) {
                try {
                    Simpson C = new Simpson();
                    
                    jTable1.setModel(new tablaAbstracta(C.lista(Double.parseDouble(N.getText()), a, b, invertir)));
                    lblSolucion.setText("" + C.getSolucion());//  Solucion.setText(""+C.getSolucion());
                } catch (InputMismatchException error) {
                    JOptionPane.showMessageDialog(null, "Ingrese un numero Input valido\n" + error.toString());
                } catch (NumberFormatException error) {
                    JOptionPane.showMessageDialog(null, "Ingrese un numero valido\n" + error.toString());
                }
            }
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

