    public class tablaAbstracta extends AbstractTableModel {

        String[] cabecera = {"Punto", "Incremento", "Formula 1", "Formula 2"};//Se crea la cabecera en un vector
        private ArrayList<Atributos> listaDatos = new ArrayList<>();
        
        public tablaAbstracta(ArrayList listaD) {
            listaDatos = listaD;
        }

        public int getRowCount()//Devuelve el numero de filas
        {
            return listaDatos.size();
        }
        
        public int getColumnCount() //Devuelve el numero de columnas
        {
            return cabecera.length;
        }
        
        public Object getValueAt(int filas, int columnas) {
            switch (columnas) {
                case 0:
                    return listaDatos.get(filas).getAtributos();
                case 1:
                    return listaDatos.get(filas).getIncre();
                case 2:
                    return listaDatos.get(filas).getFormula1();
                case 3:
                    return listaDatos.get(filas).getFormula2();
            }
            return null;
        }

        public String getColumnName(int colm) {
            return cabecera[colm];//Retonra el nomrbe de la columna
        }
    }
